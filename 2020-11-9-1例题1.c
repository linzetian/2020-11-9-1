#include<stdio.h>
int main()
{
	int i = -20;
	unsigned int j = 10;
	printf("%d\n",i+j);
	//10000000 00000000 00000000 00010100(i=20的原码)
	//11111111 11111111 11111111 11101011（反码）
	//11111111 11111111 11111111 11101100（补码）（计算机存储以补码形式保存，输出为原码）
	//00000000 00000000 00000000 00001010    ->j=10原码与补码一样（无符号数）
	//11111111 11111111 11111111 11110110  两数相加的结果（补码）
	//11111111 11111111 11111111 11110101  两数相加的结果（反码=补码-1）
	//10000000 00000000 00000000 00001010  两数相加的结果（原码），输出结果为原码，即-10
	return 0;
}